/*
875. Koko Eating Bananas

题目描述:

珂珂喜欢吃香蕉。这里有?N?堆香蕉，第 i 堆中有?piles[i]?根香蕉。警卫已经离开了，将在?H?小时后回来。


珂珂可以决定她吃香蕉的速度?K?（单位：根/小时）。每个小时，她将会选择一堆香蕉，从中吃掉 K 根。如果这堆香蕉少于 K 根，她将吃掉这堆的所有香蕉，然后这一小时内不会再吃更多的香蕉。??


珂珂喜欢慢慢吃，但仍然想在警卫回来前吃掉所有的香蕉。


返回她可以在 H 小时内吃掉所有香蕉的最小速度 K（K 为整数）。

?



示例 1：



输入: piles = [3,6,7,11], H = 8

输出: 4


示例?2：



输入: piles = [30,11,23,4,20], H = 5

输出: 30


示例?3：



输入: piles = [30,11,23,4,20], H = 6

输出: 23
?



提示：


1 <= piles.length <= 10^4

piles.length <= H <= 10^9

1 <= piles[i] <= 10^9




注解：
1、启发式：K从1开始递增直到找到合适值，效率不佳；使用二分查找，快速收缩范围找到目标K。

2、如何尽量缩小初始的K范围：K最大值不会超过最大的piles[i]；K最小值不会小于sum（piles[i]）/H，忽略可能的舍入误差；

3、求取H’时要注意向上取整
*/

#include <stdio.h>

int canEat(int* piles, int pilesSize, int speed, int H)
{
    int sum = 0;

    int i=0; 
    for(i=0; i< pilesSize; i++)
    {
        sum += (piles[i] + speed - 1) / speed;//向上取整

        if(sum > H) break;
    }
    return sum > H;
}

int minEatingSpeed(int* piles, int pilesSize, int H)
{
    int maxVal = 0;
	int sum = 0;
    int i=0; 
    for(i=0; i< pilesSize; i++)
    {
        maxVal = piles[i] > maxVal ? piles[i] : maxVal;
	sum += piles[i];
    }
	
    int left = 1;
    int count = 0;
    //int left = (sum + H - 1)/ H;//未考虑可能的溢出，优化最小值
    int right = maxVal;
    while(left < right)
    {
        int mid = left + (right - left)/2;
        if(canEat(piles, pilesSize, mid, H))
	{
            left = mid + 1;
	    count++;
	}
        else
	{
            right = mid;
	    count++;
	}
    }

    printf("count=%d\n", count);
    return left;
}


int main(int argc, char* argv[])
{
    int piles[] = {3,6,7,11};
    int H = 8;
    int size = sizeof(piles)/sizeof(int);
    int K = minEatingSpeed(piles, size, H);
    printf("K=%d\n", K);

    return 0;
}